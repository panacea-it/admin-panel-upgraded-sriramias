/** Dedicated verification queue demo data (independent of payment reports list) */

const now = new Date()
const daysAgo = (n) => {
  const d = new Date(now)
  d.setDate(d.getDate() - n)
  return d.toISOString()
}

const hoursAgo = (n) => {
  const d = new Date(now)
  d.setHours(d.getHours() - n)
  return d.toISOString()
}

