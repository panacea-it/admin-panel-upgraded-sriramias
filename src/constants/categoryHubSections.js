export const DEFAULT_CATEGORY_TAB = 'programs'

export function getTabIdFromPath(pathname) {
  const match = CATEGORY_HUB_TABS.find(
    (t) => pathname === t.path || pathname.startsWith(`${t.path}/`),
  )
  return match?.id || DEFAULT_CATEGORY_TAB
}

